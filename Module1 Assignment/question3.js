
// Function to Changed border color and width of the respective P Tag and Textarea . 
function ChangedColor()
{
    // Changed P Tag border color and width
    let pTag = document.getElementById('p');
    pTag.style.borderColor = "red";
    pTag.style.borderWidth = "2px";

    // Changed Text area border color and width
    let textArea = document.getElementById('myText');
    textArea.style.borderColor = "red";
    textArea.style.borderWidth = "2px";
}

